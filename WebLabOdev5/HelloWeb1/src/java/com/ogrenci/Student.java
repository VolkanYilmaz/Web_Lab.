

package com.ogrenci;

public class Student {
   private int yas;
   private String ad;
   private int id;

   public void setYas(int yas) {
      this.yas = yas;
   }
   public int getYas() {
      return yas;
   }

   public void setAd(String ad) {
      this.ad = ad;
   }
   public String getAd() {
      return ad;
   }

   public void setId(int id) {
      this.id = id;
   }
   public int getId() {
      return id;
   }
}